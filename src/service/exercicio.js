export  function somar( num1, num2) {
    return num1 + num2
}

export function subtrair( num1, num2) {
    return num1 - num2
}

export function multiplicar( num1, num2) {
    return num1 * num2
}

export function dividir( num1, num2) {
    return num1 / num2
}

export function raiz( num1 ) {
    return num1**0.5
}

export function potencia(num1, num2) {
    return num1**num2
}